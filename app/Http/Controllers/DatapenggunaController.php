     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        User::create([
            'nik' => $request -> nik,
            'name' => $request -> name,
            'alamat' => $request -> alamat,
            'level' => $request -> level,
            'email' => $request -> email,
            'password' => bcrypt($request->password),
           
            
         ]);
        return redirect('/datapengguna');
    }

